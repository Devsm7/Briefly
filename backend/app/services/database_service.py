def insert_user(email: str, password: str):
    """Create a new user account."""
    db = SessionLocal()
    try:
        user = User(email=email, password=password)
        db.add(user)
        db.commit()
        db.refresh(user)
        return user
    finally:
        db.close()


def insert_survey_preference(user_id: int, category: str):
    """Store a user's survey preference (category selection)."""
    db = SessionLocal()
    try:
        preference = SurveyPreference(user_id=user_id, category=category)
        db.add(preference)
        db.commit()
        db.refresh(preference)
        return preference
    finally:
        db.close()


def insert_article(
    title: str,
    description: str | None,
    url: str | None,
    category: str | None,
    source: str | None,
    published_date: str | None,
    img_url: str | None,
    language: str | None,
):
    """
    Insert a raw article into the news table.
    Fields like summary and embedding are set separately by background jobs.
    """
    db = SessionLocal()
    try:
        article = News(
            title=title,
            description=description,
            url=url,
            category=category,
            source=source,
            published_date=published_date,
            image_url=img_url,
            language=language,
        )
        db.add(article)
        db.commit()
        db.refresh(article)
        return article
    finally:
        db.close()


def get_news():
    """
    Return all articles that have a summary (fully processed).
    Uses News.summary as the signal for completion — content may be None.
    """
    db = SessionLocal()
    try:
        articles = db.query(News).filter(News.summary.isnot(None)).all()
        return [
            {
                "article_id": a.article_id,
                "title": a.title,
                "preview": a.summary or a.description,
                "cover_image": a.image_url,
                "date": a.published_date,
                "content": a.content,
                "source": a.source,
                "url": a.url,
                "category": a.category,
                "author": a.author,
            }
            for a in articles
        ]
    finally:
        db.close()


