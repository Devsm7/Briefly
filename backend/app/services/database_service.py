def log_interaction(user_id: int, article_id: int, interaction_type: str):
    """
    Record a user–article interaction (e.g. "read", "like", "share").
    Interaction data feeds interest_vector updates in recommender/interest_vector.py
    (not yet implemented).
    """
    db = SessionLocal()
    try:
        interaction = UserInteraction(
            user_id=user_id,
            article_id=article_id,
            action=interaction_type,
        )
        db.add(interaction)
        db.commit()
        db.refresh(interaction)
        return interaction
    finally:
        db.close()


